package com.site.admin.adBoard.adminBoard.vo;

public class CouponVO {
	private String coupon_no;
	private String coupon_type;
	private int coupon_discount;
    private String coupon_date;
    private String coupon_update;
    private String coupon_memo;
    private String coupon_use;
      
	public String getCoupon_use() {
		return coupon_use;
	}
	public void setCoupon_use(String coupon_use) {
		this.coupon_use = coupon_use;
	}
	public String getCoupon_no() {
		return coupon_no;
	}
	public void setCoupon_no(String coupon_no) {
		this.coupon_no = coupon_no;
	}
	public String getCoupon_type() {
		return coupon_type;
	}
	public void setCoupon_type(String coupon_type) {
		this.coupon_type = coupon_type;
	}
	public int getCoupon_discount() {
		return coupon_discount;
	}
	public void setCoupon_discount(int coupon_discount) {
		this.coupon_discount = coupon_discount;
	}
	public String getCoupon_date() {
		return coupon_date;
	}
	public void setCoupon_date(String coupon_date) {
		this.coupon_date = coupon_date;
	}
	public String getCoupon_update() {
		return coupon_update;
	}
	public void setCoupon_update(String coupon_update) {
		this.coupon_update = coupon_update;
	}
	public String getCoupon_memo() {
		return coupon_memo;
	}
	public void setCoupon_memo(String coupon_memo) {
		this.coupon_memo = coupon_memo;
	}
}
